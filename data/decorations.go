// Decoration type to sprite+attr mapping.
// Used by the room entity rendering system.
type DecorationDef struct {
	Pixels     []byte
	WidthBytes int
	Height     int
	Attrs      []byte // attribute grid (w_cells × h_cells), may be nil
	AttrW      int    // width in character cells
	AttrH      int    // height in character cells
}

// DecorationByType maps entity graphic type to its sprite data.
// Only populated types are included; others return nil from GetDecoration.
