package data

// DoorType identifies what kind of door this is.
type DoorType byte

const (
	DoorBlue   DoorType = 0x01 // Standard blue castle door
	DoorNormal DoorType = 0x02 // Standard door (most common)
	DoorSerf   DoorType = 0x03 // Serf secret passage
	DoorRed    DoorType = 0x08 // Red locked door
	DoorGreen  DoorType = 0x09 // Green locked door
	DoorCyan   DoorType = 0x0A // Cyan locked door
	DoorYellow DoorType = 0x0B // Yellow locked door
	DoorRedC   DoorType = 0x0C // Red castle door
	DoorGreenC DoorType = 0x0D // Green castle door
	DoorCyanC  DoorType = 0x0E // Cyan castle door
)

// DoorSide represents one side of a door pair (8 raw bytes from Z80 source).
type DoorSide [8]byte

func (d DoorSide) Type() byte { return d[0] }
func (d DoorSide) Room() byte { return d[1] }
func (d DoorSide) X() byte    { return d[3] }
func (d DoorSide) Y() byte    { return d[4] }

// DoorPair is a bidirectional door link.
type DoorPair struct {
	A DoorSide // side A
	B DoorSide // side B
}

