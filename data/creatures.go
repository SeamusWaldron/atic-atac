// CreatureSpriteFrames returns 2 animation frames for a creature kind.
func CreatureSpriteFrames(kind int) (frame1, frame2 []byte) {
	switch kind & 0x0F {
	case 0, 8: // Spider
		return SpiderSprite1, SpiderSprite2
	case 2, 3: // Bat
		return BatSprite1, BatSprite2
	case 10: // Blob
		return BlobSprite1, BlobSprite2
	case 14: // Ghost
		return GhostSprite1, GhostSprite2
	default:
		// Fallback to spider for unimplemented types
		return SpiderSprite1, SpiderSprite2
	}
}

// PopFrames returns the explosion animation sprite for a given frame index (0-3).
func PopFrames(frame int) []byte {
	switch frame & 0x03 {
	case 0:
		return PopSprite1
	case 1:
		return PopSprite2
	case 2:
		return PopSprite3
	default:
		return PopSprite4
	}
}
