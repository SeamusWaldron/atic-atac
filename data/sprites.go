// CharacterSprites returns the sprite set for the given character class.
func CharacterSprites(c CharacterClass) *[4][3][]byte {
	switch c {
	case Wizard:
		return &WizardSprites
	case Knight:
		return &KnightSprites
	case Serf:
		return &SerfSprites
	default:
		return &KnightSprites
	}
}

// AnimFrame returns the animation frame index for a given walk counter.
// Pattern: 0, 1, 2, 1, 0, 1, 2, 1, ...
func AnimFrame(walkCounter int) int {
	frames := [4]int{0, 1, 2, 1}
	return frames[walkCounter%4]
}
