// DrawChar draws a single character at pixel position (x, y) into the buffer.
// ch is an ASCII character (32-127). Uses OR mode.
func (b *Buffer) DrawChar(x, y int, ch byte) {
	if ch < 32 || ch > 127 {
		return
	}
	idx := int(ch-32) * 8
	col := x >> 3
	for row := 0; row < 8; row++ {
		py := y + row
		if py < 0 || py >= ScreenHeightPx {
			continue
		}
		addr := yTable[py] + uint16(col)
		if int(addr) < DisplaySize {
			b.Pixels[addr] |= CharSet[idx+row]
		}
	}
}

// DrawCharFrom draws an 8×8 character glyph from arbitrary data at pixel position (x, y).
// charData must be 8 bytes. Uses OR mode.
func (b *Buffer) DrawCharFrom(x, y int, charData []byte) {
	if len(charData) < 8 {
		return
	}
	col := x >> 3
	shift := uint(x & 7)
	for row := 0; row < 8; row++ {
		py := y + row
		if py < 0 || py >= ScreenHeightPx {
			continue
		}
		addr := int(yTable[py]) + col
		if shift == 0 {
			if addr >= 0 && addr < DisplaySize {
				b.Pixels[addr] |= charData[row]
			}
		} else {
			if addr >= 0 && addr < DisplaySize {
				b.Pixels[addr] |= charData[row] >> shift
			}
			if addr+1 >= 0 && addr+1 < DisplaySize {
				b.Pixels[addr+1] |= charData[row] << (8 - shift)
			}
		}
	}
}

// DrawString draws a string at pixel position (x, y) into the buffer.
// Each character is 8 pixels wide.
func (b *Buffer) DrawString(x, y int, s string) {
	for i := 0; i < len(s); i++ {
		b.DrawChar(x+i*8, y, s[i])
	}
}

// DrawStringFrom draws a string using a custom charset (96 chars, 8 bytes each).
// charset[0] = space (char 32). Each character is 8 pixels wide.
func (b *Buffer) DrawStringFrom(x, y int, s string, charset *[96][8]byte) {
	for i := 0; i < len(s); i++ {
		ch := s[i]
		if ch < 32 || ch > 127 {
			continue
		}
		idx := int(ch - 32)
		b.DrawCharFrom(x+i*8, y, charset[idx][:])
	}
}
